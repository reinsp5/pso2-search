<script lang="ts" setup>
import {
  collection,
  addDoc,
  Timestamp,
  getDocs,
  query,
  where,
  runTransaction,
  getDoc,
  doc,
} from "firebase/firestore";
import type { VForm } from "vuetify/lib/components/index.mjs";
import { v4 as uuidv4 } from "uuid";
import { Item } from "~/types/item";
import { DirectUploadUrlResponse } from "~/types/cloudflare";

// 認証必須
// definePageMeta({
//   middleware: ["auth"],
// });

// SEO情報
useSeoMeta({
  title: "アイテム情報登録 | PSO2 Search Unofficial Item Search Engine",
});

// 入力情報の共有State
const itemInfo = useInsertItemInfo();
console.log(typeof itemInfo.value);

// アイテムフォーム
const itemCreateForm = ref<VForm | null>(null);
const images = useState<File[]>("preview-images");

// ローディング
const loading = ref(false);

// 登録する
const createItem = async () => {
  loading.value = true;
  if (!itemCreateForm.value) {
    loading.value = false;
    return;
  }
  // バリデーション
  const isValid = await itemCreateForm.value.validate();
  if (!isValid.valid) {
    loading.value = false;
    return;
  }

  // イメージURL
  const previewUrl = useState<string>("preview-url", () => "");

  // 画像が選択されている場合はアップロード
  const upload = ref<DirectUploadUrlResponse>();
  if (previewUrl.value != "") {
    // 画像アップロード用のURLを取得
    const { getUploadUrl } = useImageUpload();
    const uploadUrl = getUploadUrl();

    // 画像アップロード用のURLが取得できなかったらエラー
    if (!uploadUrl) {
      loading.value = false;
      return;
    } else if (!uploadUrl.success) {
      loading.value = false;
      return;
    }

    // 画像をアップロード
    const response = await fetch(previewUrl.value);
    const fileBlob = await response.blob();
    const formData = new FormData();
    formData.append("file", fileBlob);
    const { data } = await useFetch<DirectUploadUrlResponse>(
      uploadUrl.uploadURL,
      {
        method: "POST",
        body: formData,
      }
    );

    // 画像のアップロードが失敗したらエラー
    if (!data.value) {
      throw new Error("画像のアップロードに失敗しました。");
    } else if (!data.value!.success) {
      throw new Error("画像のアップロードに失敗しました。");
    }

    upload.value = data.value;
  }

  // データベースに登録
  const { $auth, $store } = useNuxtApp();
  try {
    // <<トランザクション開始>>
    await runTransaction($store, async (transaction) => {
      // itemsコレクションを取得
      const querySnapshot = await getDocs(
        query(
          collection($store, "items"),
          where("name", "==", itemInfo.value.name)
        )
      );
      // 既に登録されている場合はエラー
      if (!querySnapshot.empty) {
        throw new Error("既に登録されている武器です。");
      }

      // ユーザ情報を取得
      const user = $auth.currentUser;
      if (!user) {
        throw new Error("ユーザ情報が取得できませんでした。");
      } else if (!user.uid) {
        throw new Error("ユーザ情報が取得できませんでした。");
      }
      const userDoc = await getDoc(doc($store, "users", user.uid));
      if (!userDoc.exists()) {
        throw new Error("ユーザ情報が取得できませんでした。");
      }
      const uuid = uuidv4();

      // ドキュメントを追加
      const docRef = await addDoc(collection($store, "items"), {
        ...itemInfo.value.toJSON(),
        id: uuid,
        create_user: userDoc.get("displayName") ?? "unknown",
        update_user: userDoc.get("displayName") ?? "unknown",
        created_at: Timestamp.now(),
        updated_at: Timestamp.now(),
        cover_image_url: {
          id: upload.value?.result.id ?? "",
          url: upload.value
            ? `https://imagedelivery.net/y6deFg4uWz5Imy5sDx3EYA/${
                upload.value!.result.id
              }/public`
            : "https://pso2-search.com/images/no_image.webp",
        },
      });
    });
    // <<トランザクション終了>>

    // フォームをリセット
    itemInfo.value = new Item();
    previewUrl.value = "";
    images.value = [];
  } catch (e: unknown) {
    if (e instanceof Error) {
      console.error(e.message);
    }
  } finally {
    loading.value = false;
  }
};
</script>

<template>
  <v-container class="h-100">
    <v-row class="h-100">
      <v-col>
        <v-card class="mx-auto" variant="flat" max-width="1280">
          <v-card-title class="d-flex align-center justify-center text-h5">
            アイテム登録
          </v-card-title>
          <CreateForm />
        </v-card>
      </v-col>
    </v-row>
  </v-container>
</template>

